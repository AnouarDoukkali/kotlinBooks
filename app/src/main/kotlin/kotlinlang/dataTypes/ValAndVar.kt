package kotlinlang.dataTypes

private fun variables() {
    // an immutable variable is declared by using val keyword
    val a = 5

    // a mutable variable is declared by using var keyword
    var b = 10

    // a variable can be reassigned
    b = 20

    // Kotlin has a smart type inference system that can determine the type of variable
    val name = "anouar"
}
